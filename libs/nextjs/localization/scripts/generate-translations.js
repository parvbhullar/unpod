// Function to get translation or use English as fallback
function getTranslation(text, lang) {
  const langMap = translationMaps[lang];
  if (langMap && langMap[text]) {
    return langMap[text];
  }
  // For untranslated strings, keep original English
  // In production, you'd want to use a translation API
  return text;
}

// Read en_US.json
const enPath = path.join(__dirname, '../src/locales/en_US.json');
const enData = JSON.parse(fs.readFileSync(enPath, 'utf8'));

// Language configurations
const languages = {
  'ar_SA': 'ar',
  'bn_BD': 'bn',
  'es_ES': 'es',
  'fr_FR': 'fr',
  'hi_IN': 'hi',
  'ja_JP': 'ja',
  'pt_PT': 'pt',
  'ru_RU': 'ru',
  'zh_CN': 'zh',
};

console.log('Starting translation process...\n');

// Generate each language file
Object.entries(languages).forEach(([locale, langCode]) => {
  console.log(`Translating to ${locale} (${langCode})...`);

  const translated = {};
  let translatedCount = 0;
  let totalCount = 0;

  Object.entries(enData).forEach(([key, value]) => {
    totalCount++;
    const translatedValue = getTranslation(value, langCode);
    translated[key] = translatedValue;

    if (translatedValue !== value) {
      translatedCount++;
    }
  });

  // Write translated file
  const targetPath = path.join(__dirname, `../src/locales/${locale}.json`);
  fs.writeFileSync(targetPath, JSON.stringify(translated, null, 2), 'utf8');

  console.log(`  ✓ Created ${locale}.json (${translatedCount}/${totalCount} strings translated)\n`);
});

console.log('Translation complete!');
